export function mergeWithDemoData<T extends { id: string }>(
  realApplications: T[] | undefined,
  demos: DemoApplication[]
): (T | DemoApplication)[] {
  if (!realApplications || realApplications.length === 0) {
    return demos;
  }
  return realApplications;
}
